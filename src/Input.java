
public class Input {
	int time; 
	String body; 
	
	public Input(int t, String s) {
		this.time = t; 
		this.body = s; 
	}

	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}

}
